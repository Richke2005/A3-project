    public boolean verifyVoidRows(){
        boolean isVoid = true;
        if(txtCpf.getText().isEmpty()){
            showMessageDialog(null, "Digite o CPF para continuar");
            isVoid = false;
        }
        if(txtName.getText().isEmpty()){
            showMessageDialog(null, "Digite o nome para continuar");
        isVoid = false;
        }
        return isVoid;
    }
